DATA SEGMENT
  NUM1 DW 12FFH
  NUM2 DW 0100H
DATA ENDS

CODE SEGMENT
  ASSUME CS:CODE,DS:DATA
  START:
    MOV AX,DATA
    MOV DS,AX

    MOV AX,NUM1
    MOV BX,NUM2

    ADD AX,BX

    MOV CX,0
    MOV BX,10

  DIVIDE:
    MOV DX,0
    DIV BX
    PUSH DX
    INC CX
    CMP AX,0
    JNE DIVIDE

  PRINT:
    POP DX
    ADD DL,30H
    MOV AH,02H
    INT 21H
    LOOP PRINT

CODE ENDS
END START
